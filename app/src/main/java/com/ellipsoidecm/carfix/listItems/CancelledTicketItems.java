public class CancelledTicketItems {

    private String cancelledTicketId;
    private String cancelledTicketStatus;
    private String cancelledImageUrl;

    public String getCancelledTicketId() {
        return cancelledTicketId;
    }

    public void setCancelledTicketId(String cancelledTicketId) {
        this.cancelledTicketId = cancelledTicketId;
    }

    public String getCancelledTicketStatus() {
        return cancelledTicketStatus;
    }

    public void setCancelledTicketStatus(String cancelledTicketStatus) {
        this.cancelledTicketStatus = cancelledTicketStatus;
    }

    public String getCancelledImageUrl() {
        return cancelledImageUrl;
    }

    public void setCancelledImageUrl(String cancelledImageUrl) {
        this.cancelledImageUrl = cancelledImageUrl;
    }
}
