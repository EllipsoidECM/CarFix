public class CompletedTicketItems {

    private String completedTicketId;
    private String completedTicketStatus;
    private String completedImageUrl;

    public String getCompletedTicketId() {
        return completedTicketId;
    }

    public void setCompletedTicketId(String completedTicketId) {
        this.completedTicketId = completedTicketId;
    }

    public String getCompletedTicketStatus() {
        return completedTicketStatus;
    }

    public void setCompletedTicketStatus(String completedTicketStatus) {
        this.completedTicketStatus = completedTicketStatus;
    }

    public String getCompletedImageUrl() {
        return completedImageUrl;
    }

    public void setCompletedImageUrl(String completedImageUrl) {
        this.completedImageUrl = completedImageUrl;
    }
}
